local function AddSingleFile()
    local retval, filename = reaper.GetUserFileNameForRead(scan_path, "Select Track Template", "*.RTrackTemplate")
    if retval then
        local new_file_name = string.lower(GetFileName(filename))
        local exists = false
        for _, item in ipairs(db_items) do
            if string.lower(GetFileName(item.file_path)) == new_file_name then exists = true break end
        end
        if not exists then
            local name_with_ext = filename:match("([^/\\]+)$")
            local name_no_ext = name_with_ext:gsub("%.RTrackTemplate$", "")
            table.insert(db_items, {
                id = "tpl_" .. tostring(math.random(100000, 999999)), file_path = filename, display_name = name_no_ext, 
                tree_path_str = "", target_folder = "", tags_str = "", sends = {}
            })
            needs_update = true
        end
    end
end

local function ScanDirectory(current_path, base_path)
    local i = 0
    while true do
        local file = reaper.EnumerateFiles(current_path, i)
        if not file then break end
        if string.match(file, "%.RTrackTemplate$") then
            local full_path = current_path .. "/" .. file
            local new_file_name = string.lower(GetFileName(full_path))
            local exists = false
            for _, item in ipairs(db_items) do
                if string.lower(GetFileName(item.file_path)) == new_file_name then exists = true break end
            end
            if not exists then
                local name_no_ext = string.gsub(file, "%.RTrackTemplate$", "")
                local relative_path = string.sub(current_path, string.len(base_path) + 2)
                local auto_tree_str = ""
                if relative_path and relative_path ~= "" then auto_tree_str = string.gsub(string.gsub(relative_path, "\\", "/"), "/", " / ") end
                table.insert(db_items, { id = "tpl_" .. tostring(math.random(100000, 999999)), file_path = full_path, display_name = name_no_ext, tree_path_str = auto_tree_str, target_folder = "", tags_str = "", sends = {} })
                needs_update = true
            end
        end
        i = i + 1
    end
    local j = 0
    while true do
        local subdir = reaper.EnumerateSubdirectories(current_path, j)
        if not subdir then break end
        ScanDirectory(current_path .. "/" .. subdir, base_path)
        j = j + 1
    end
end

