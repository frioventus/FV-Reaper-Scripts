local function NormalizePath(p)
    if not p then return "" end
    return string.lower(string.gsub(p, "\\", "/"))
end

local function GetFileName(path)
    if not path or path == "" then return "" end
    return path:match("([^/\\]+)$") or path
end

local function SplitString(inputstr, sep)
    if sep == nil then sep = "%s" end
    local t = {}
    for str in string.gmatch(inputstr, "([^"..sep.."]+)") do
        str = str:match("^%s*(.-)%s*$")
        if str ~= "" then table.insert(t, str) end
    end
    return t
end

if reaper.file_exists(db_filepath) then
    local loaded_data = dofile(db_filepath)
    for _, item in ipairs(loaded_data) do
        item.tree_path_str = item.tree_path and table.concat(item.tree_path, " / ") or ""
        item.tags_str = item.tags and table.concat(item.tags, ", ") or ""
        item.target_folder = item.target_folder or ""
        item.sends = (item.routing and item.routing.sends) and item.routing.sends or {}
        table.insert(db_items, item)
    end
end

