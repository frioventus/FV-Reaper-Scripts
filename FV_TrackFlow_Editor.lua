                reaper.ImGui_TableNextRow(ctx)
                reaper.ImGui_TableSetColumnIndex(ctx, 0)
                reaper.ImGui_PushStyleColor(ctx, reaper.ImGui_Col_Text(), 0x888888FF)
                reaper.ImGui_Selectable(ctx, "  [ + Root Directory ]", false, reaper.ImGui_SelectableFlags_SpanAllColumns())
                reaper.ImGui_PopStyleColor(ctx)
                
                if reaper.ImGui_BeginDragDropTarget(ctx) then
                    local rv, payload_str = reaper.ImGui_AcceptDragDropPayload(ctx, 'DND_OBJ')
                    if rv and payload_str then
                        local src_type, src_id = payload_str:match("^(%w+)|(.*)$")
                        pending_move = { src_type = src_type, src_id = src_id, dst_parent = "" }
                    end
                    reaper.ImGui_EndDragDropTarget(ctx)
                end
            end

            DrawNodeContent(cached_tree, "", cached_tree)
            
            reaper.ImGui_EndTable(ctx)
        end
        
        reaper.ImGui_PopStyleColor(ctx, 2)
        reaper.ImGui_PopStyleVar(ctx)
        
        if pending_move then
            local safe = true
            if pending_move.src_type == "FOLDER" then
                if pending_move.dst_parent == pending_move.src_id or
                   string.sub(pending_move.dst_parent, 1, #pending_move.src_id + 1) == pending_move.src_id .. "/" then
                   safe = false
                end
            end
            
            if safe then
                local moved_obj, _, _ = RemoveNode(cached_tree, pending_move.src_type, pending_move.src_id, "")
                if moved_obj then
                    local target_parent_node = GetNodeByPath(cached_tree, pending_move.dst_parent) or cached_tree
                    table.insert(target_parent_node.children, moved_obj)
                end
            end
            
            RebuildDBFromTree()
            UpdateEditorCache()
            pending_move = nil
        end
        
        if show_tag_modal then reaper.ImGui_OpenPopup(ctx, "Tag Color & Name Manager"); show_tag_modal = false end
        reaper.ImGui_SetNextWindowSizeConstraints(ctx, 350, 200, 350, 500)
        if reaper.ImGui_BeginPopupModal(ctx, "Tag Color & Name Manager", nil, reaper.ImGui_WindowFlags_AlwaysAutoResize()) then
            reaper.ImGui_TextDisabled(ctx, "Set custom colors and short names for tags.")
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            
            reaper.ImGui_BeginChild(ctx, "TagsScroll", 0, 300, 0)
            for _, tag in ipairs(sorted_unique_tags) do
                reaper.ImGui_AlignTextToFramePadding(ctx)
                reaper.ImGui_Text(ctx, tag)
                
                reaper.ImGui_SameLine(ctx, 130)
                reaper.ImGui_PushID(ctx, "sh_"..tag)
                reaper.ImGui_PushItemWidth(ctx, 60)
                local rv_s, ns = reaper.ImGui_InputText(ctx, "##short", tag_configs[tag].short)
                if rv_s then tag_configs[tag].short = string.sub(ns, 1, 4) end
                reaper.ImGui_PopItemWidth(ctx)
                reaper.ImGui_PopID(ctx)
                
                reaper.ImGui_SameLine(ctx, 200)
                reaper.ImGui_PushID(ctx, "col_"..tag)
                local c_flags = reaper.ImGui_ColorEditFlags_NoInputs() | reaper.ImGui_ColorEditFlags_AlphaBar()
                local rv_c, nc = reaper.ImGui_ColorEdit4(ctx, "Color", tag_configs[tag].color, c_flags)
                if rv_c then tag_configs[tag].color = nc end
                reaper.ImGui_PopID(ctx)
            end
            reaper.ImGui_EndChild(ctx)
            
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            if reaper.ImGui_Button(ctx, "Save & Close", 120) then
                SaveTags()
                needs_update = true
                reaper.ImGui_CloseCurrentPopup(ctx)
            end
            reaper.ImGui_SameLine(ctx)
            if reaper.ImGui_Button(ctx, "Cancel", 100) then reaper.ImGui_CloseCurrentPopup(ctx) end
            reaper.ImGui_EndPopup(ctx)
        end
        
        if show_rename_modal and folder_to_rename then
            reaper.ImGui_OpenPopup(ctx, "Rename Folder")
            show_rename_modal = false
        end
        if reaper.ImGui_BeginPopupModal(ctx, "Rename Folder", nil, reaper.ImGui_WindowFlags_AlwaysAutoResize()) then
            reaper.ImGui_Text(ctx, "Rename all items in folder:\n[" .. folder_to_rename .. "]")
            reaper.ImGui_Spacing(ctx)
            reaper.ImGui_PushItemWidth(ctx, 350)
            local rv, text = reaper.ImGui_InputText(ctx, "##newfldname", new_folder_name)
            if rv then new_folder_name = text end
            reaper.ImGui_PopItemWidth(ctx)
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            if reaper.ImGui_Button(ctx, "Save & Apply", 150) then
                for _, item in ipairs(db_items) do
                    if item.tree_path_str == folder_to_rename then
                        item.tree_path_str = new_folder_name
                    elseif string.sub(item.tree_path_str, 1, string.len(folder_to_rename) + 1) == folder_to_rename .. "/" then
                        item.tree_path_str = new_folder_name .. string.sub(item.tree_path_str, string.len(folder_to_rename) + 1)
                    end
                end
                needs_update = true
                reaper.ImGui_CloseCurrentPopup(ctx)
            end
            reaper.ImGui_SameLine(ctx)
            if reaper.ImGui_Button(ctx, "Cancel", 100) then reaper.ImGui_CloseCurrentPopup(ctx) end
            reaper.ImGui_EndPopup(ctx)
        end
        
        if show_new_folder_modal then
            reaper.ImGui_OpenPopup(ctx, "Create New Folder")
            show_new_folder_modal = false
        end
        if reaper.ImGui_BeginPopupModal(ctx, "Create New Folder", nil, reaper.ImGui_WindowFlags_AlwaysAutoResize()) then
            reaper.ImGui_Text(ctx, "Enter new folder path (Use '/' for subfolders):")
            reaper.ImGui_Spacing(ctx)
            reaper.ImGui_PushItemWidth(ctx, 350)
            local rv, text = reaper.ImGui_InputText(ctx, "##createfldname", new_empty_folder_name)
            if rv then new_empty_folder_name = text end
            reaper.ImGui_PopItemWidth(ctx)
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            if reaper.ImGui_Button(ctx, "Create", 100) then
                table.insert(db_items, { id = "dir_" .. tostring(math.random(100000, 999999)), file_path = "", display_name = "(Empty Folder)", tree_path_str = new_empty_folder_name, target_folder = "", tags_str = "", sends = {} })
                needs_update = true
                reaper.ImGui_CloseCurrentPopup(ctx)
            end
            reaper.ImGui_SameLine(ctx)
            if reaper.ImGui_Button(ctx, "Cancel", 100) then reaper.ImGui_CloseCurrentPopup(ctx) end
            reaper.ImGui_EndPopup(ctx)
        end
        
        if show_edit_modal and editing_item then
            reaper.ImGui_OpenPopup(ctx, "Edit Track Template")
            show_edit_modal = false
        end
        reaper.ImGui_SetNextWindowSize(ctx, 450, 0, reaper.ImGui_Cond_Appearing())
        if reaper.ImGui_BeginPopupModal(ctx, "Edit Track Template", nil, reaper.ImGui_WindowFlags_NoSavedSettings()) then
            local item = editing_item
            reaper.ImGui_TextDisabled(ctx, "File: " .. (item.file_path == "" and "Virtual Folder" or item.file_path))
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            if item.file_path ~= "" then
                reaper.ImGui_PushItemWidth(ctx, -1)
                reaper.ImGui_Text(ctx, "Track Name:")
                local rv1, n_name = reaper.ImGui_InputText(ctx, "##m_name", item.display_name)
                if rv1 then item.display_name = n_name; needs_update = true end
            end
            
            reaper.ImGui_Spacing(ctx)
            reaper.ImGui_Text(ctx, "Folder Path (Separate with '/'):")
            local rv2, n_tree = reaper.ImGui_InputText(ctx, "##m_tree", item.tree_path_str)
            if rv2 then item.tree_path_str = n_tree; needs_update = true end
            
            if item.file_path ~= "" then
                reaper.ImGui_Spacing(ctx)
                reaper.ImGui_Text(ctx, "Auto-Insert Target Track (REAPER):")
                local rv_tgt, n_tgt = reaper.ImGui_InputText(ctx, "##m_target", item.target_folder or "")
                if rv_tgt then item.target_folder = n_tgt; needs_update = true end
                if reaper.ImGui_IsItemHovered(ctx) then reaper.ImGui_SetTooltip(ctx, "If this REAPER track exists, template will be inserted inside it automatically.") end
                
                reaper.ImGui_Spacing(ctx)
                reaper.ImGui_Text(ctx, "Tags (Separate with ','):")
                local rv3, n_tags = reaper.ImGui_InputText(ctx, "##m_tags", item.tags_str)
                if rv3 then item.tags_str = n_tags; needs_update = true end
                reaper.ImGui_PopItemWidth(ctx)
                
                reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
                reaper.ImGui_Text(ctx, "FX SENDS:")
                reaper.ImGui_SameLine(ctx)
                if reaper.ImGui_Button(ctx, "+ Add Send") then table.insert(item.sends, {target = "", level_db = 0.0}); needs_update = true end
                local sends_to_remove = {}
                for s_idx, send in ipairs(item.sends) do
                    reaper.ImGui_PushID(ctx, "m_send_" .. s_idx)
                    reaper.ImGui_PushItemWidth(ctx, 150)
                    local rvt, nt = reaper.ImGui_InputText(ctx, "Target", send.target)
                    if rvt then send.target = nt; needs_update = true end
                    reaper.ImGui_PopItemWidth(ctx)
                    reaper.ImGui_SameLine(ctx)
                    reaper.ImGui_PushItemWidth(ctx, 80)
                    local rvl, nl = reaper.ImGui_InputDouble(ctx, "dB", send.level_db, 0.5, 1.0, "%.1f")
                    if rvl then send.level_db = nl; needs_update = true end
                    reaper.ImGui_PopItemWidth(ctx)
                    reaper.ImGui_SameLine(ctx)
                    if reaper.ImGui_Button(ctx, "X") then table.insert(sends_to_remove, s_idx) end
                    reaper.ImGui_PopID(ctx)
                end
                for r = #sends_to_remove, 1, -1 do table.remove(item.sends, sends_to_remove[r]); needs_update = true end
            end
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            if reaper.ImGui_Button(ctx, "Done", 100) then needs_update = true; reaper.ImGui_CloseCurrentPopup(ctx) end
            reaper.ImGui_EndPopup(ctx)
        end

        if show_delete_modal and item_to_delete then
            reaper.ImGui_OpenPopup(ctx, "Confirm Delete")
            show_delete_modal = false
        end
        if reaper.ImGui_BeginPopupModal(ctx, "Confirm Delete", nil, reaper.ImGui_WindowFlags_AlwaysAutoResize()) then
            local del_item = item_to_delete
            reaper.ImGui_Text(ctx, "Are you sure you want to permanently remove:\n\n'" .. (del_item and del_item.display_name or "Unknown") .. "'\n\nfrom the database?")
            reaper.ImGui_Spacing(ctx); reaper.ImGui_Separator(ctx); reaper.ImGui_Spacing(ctx)
            reaper.ImGui_PushStyleColor(ctx, reaper.ImGui_Col_Button(), 0xAA3333FF)
            reaper.ImGui_PushStyleColor(ctx, reaper.ImGui_Col_ButtonHovered(), 0xCC4444FF)
            if reaper.ImGui_Button(ctx, "Yes, Delete It", 150) then
                for j, it in ipairs(db_items) do
                    if it == del_item then table.remove(db_items, j) break end
                end
                needs_update = true
                item_to_delete = nil
                reaper.ImGui_CloseCurrentPopup(ctx)
            end
            reaper.ImGui_PopStyleColor(ctx, 2)
            reaper.ImGui_SameLine(ctx)
            if reaper.ImGui_Button(ctx, "Cancel", 100) then item_to_delete = nil; reaper.ImGui_CloseCurrentPopup(ctx) end
            reaper.ImGui_EndPopup(ctx)
        end

        reaper.ImGui_End(ctx)
    end
    
