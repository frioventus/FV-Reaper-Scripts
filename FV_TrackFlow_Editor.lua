local function UpdateEditorCache()
    cached_tree = { children = {}, folders = {} }
    local srch = search_text:lower()
    for _, item in ipairs(db_items) do
        local show_item = true
        if srch ~= "" then
            if not string.find(string.lower(item.display_name), srch) and not string.find(string.lower(item.tree_path_str), srch) then show_item = false end
        end
        if show_item then
            local parts = {}
            if item.tree_path_str and item.tree_path_str ~= "" then
                parts = SplitString(item.tree_path_str, "/")
            end
            
            local current_node = cached_tree
            for _, part in ipairs(parts) do
                part = part:match("^%s*(.-)%s*$")
                if not current_node.folders[part] then
                    local new_fld = { children = {}, folders = {}, name = part }
                    current_node.folders[part] = new_fld
                    table.insert(current_node.children, { type = "folder", name = part, node = new_fld })
                end
                current_node = current_node.folders[part]
            end
            table.insert(current_node.children, { type = "item", item = item })
        end
    end
end

local function RebuildDBFromTree()
    local new_db = {}
    local function Flatten(node, current_path)
        for _, child in ipairs(node.children) do
            if child.type == "item" then
                child.item.tree_path_str = current_path
                table.insert(new_db, child.item)
            elseif child.type == "folder" then
                local next_path = current_path == "" and child.name or (current_path .. "/" .. child.name)
                Flatten(child.node, next_path)
            end
        end
    end
    Flatten(cached_tree, "")
    db_items = new_db
    needs_update = true
end

local function RemoveNode(node, p_type, p_id, current_path)
    for i, child in ipairs(node.children) do
        if child.type == "item" and p_type == "ITEM" and child.item.id == p_id then
            return table.remove(node.children, i), i, node
        elseif child.type == "folder" and p_type == "FOLDER" then
            local f_path = current_path == "" and child.name or (current_path .. "/" .. child.name)
            if f_path == p_id then return table.remove(node.children, i), i, node end
        end
    end
    for _, child in ipairs(node.children) do
        if child.type == "folder" then
            local f_path = current_path == "" and child.name or (current_path .. "/" .. child.name)
            local obj, idx, p_node = RemoveNode(child.node, p_type, p_id, f_path)
            if obj then return obj, idx, p_node end
        end
    end
    return nil, nil, nil
end

local function GetNodeByPath(node, path)
    if path == "" then return node end
    local parts = SplitString(path, "/")
    local current = node
    for _, part in ipairs(parts) do
        part = part:match("^%s*(.-)%s*$")
        if current.folders[part] then current = current.folders[part]
        else return nil end
    end
    return current
end

