local function SaveDatabase()
    local file = io.open(db_filepath, "w")
    if not file then return false end
    file:write("-- Auto-generated by FV_TrackFlow_Editor\nreturn {\n")
    for _, item in ipairs(db_items) do
        file:write("  {\n")
        file:write("    id = \"" .. item.id .. "\",\n")
        file:write("    display_name = \"" .. item.display_name .. "\",\n")
        local safe_path = string.gsub(item.file_path, "\\", "/")
        file:write("    file_path = \"" .. safe_path .. "\",\n")
        file:write("    target_folder = \"" .. (item.target_folder or "") .. "\",\n")
        
        file:write("    tree_path = {")
        if item.tree_path_str ~= "" then
            local t_path = SplitString(item.tree_path_str, "/")
            for i, p in ipairs(t_path) do file:write("\"" .. p .. "\"" .. (i < #t_path and ", " or "")) end
        end
        file:write("},\n    tags = {")
        
        local t_tags = SplitString(item.tags_str, ",")
        for i, t in ipairs(t_tags) do file:write("\"" .. string.upper(t) .. "\"" .. (i < #t_tags and ", " or "")) end
        file:write("},\n    routing = {\n      sends = {\n")
        
        for _, send in ipairs(item.sends) do
            if send.target and send.target ~= "" then
                file:write("        { target = \"" .. send.target .. "\", level_db = " .. tostring(send.level_db) .. " },\n")
            end
        end
        file:write("      }\n    }\n  },\n")
    end
    file:write("}\n")
    file:close()
    return true
end

local function SaveTags()
    local f = io.open(tags_filepath, "w")
    if f then
        f:write("-- Auto-generated tag configurations\nreturn {\n")
        for tag, data in pairs(tag_configs) do
            f:write(string.format("  [\"%s\"] = { short = \"%s\", color = 0x%08X },\n", tag, data.short, data.color & 0xFFFFFFFF))
        end
        f:write("}\n")
        f:close()
    end
end

